function getValue(source, path) {
  return path.split(".").reduce((value, key) => value && value[key], source);
}

function getDialogRoot() {
  return document.querySelector("[data-finder-dialog]");
}

function isFinderComplete() {
  return Boolean(finderState.audience && finderState.agesConfirmed && finderState.ages.length && finderState.groupSize && finderState.style);
}

function getYoungestAge() {
  return ageOrder.find((age) => finderState.ages.includes(age)) || null;
}

function audienceLabel(value) {
  return value ? copy[currentLanguage].audienceLabels[value] : "";
}

function isStepAnswered(step) {
  if (step === 0) return Boolean(finderState.audience);
  if (step === 1) return Boolean(finderState.ages.length);
  if (step === 2) return Boolean(finderState.groupSize);
  return Boolean(finderState.style);
}

function getQuestionSummary(question) {
  const strings = copy[currentLanguage];
  if (question === "audience") return finderState.audience ? audienceLabel(finderState.audience) : "";
  if (question === "ages") return finderState.ages.map((age) => strings.ageLabels[age]).join(" · ");
  if (question === "groupSize") return finderState.groupSize ? strings.groupLabels[finderState.groupSize] : "";
  if (question === "style") {
    const styleKey = finderState.style === "adventure" ? "styleAdventure" : "style" + (finderState.style ? finderState.style.charAt(0).toUpperCase() + finderState.style.slice(1) : "");
    return finderState.style ? strings[styleKey] : "";
  }
  return "";
}

function applyLanguage(language) {
  currentLanguage = language;
  const strings = copy[language];
  document.documentElement.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(strings, element.dataset.i18n);
    if (typeof value === "string") element.textContent = value;
  });

  document.querySelectorAll("[data-aria-i18n]").forEach((element) => {
    const value = getValue(strings, element.dataset.ariaI18n);
    if (typeof value === "string") element.setAttribute("aria-label", value);
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  const menuToggle = document.querySelector(".menu-toggle");
  if (menuToggle) {
    const menuIsOpen = menuToggle.getAttribute("aria-expanded") === "true";
    menuToggle.setAttribute("aria-label", menuIsOpen ? strings.menuClose : strings.menuOpen);
    const hiddenText = menuToggle.querySelector("[data-i18n]");
    if (hiddenText) hiddenText.textContent = menuIsOpen ? strings.menuClose : strings.menuOpen;
  }

  renderFinderState();
  renderQuestgiverMessage();
  renderPlayTypes();
}

function renderQuestProfile() {
  const root = getDialogRoot();
  if (!root) return;
  const strings = copy[currentLanguage];
  const profile = root.querySelector(".quest-profile");
  const list = root.querySelector("[data-quest-profile-list]");
  const status = root.querySelector("[data-profile-status]");
  if (!profile || !list || !status) return;

  const answers = [
    { step: 0, label: strings.questionOneTitle, value: getQuestionSummary("audience") },
    { step: 1, label: strings.questionTwoTitle, value: getQuestionSummary("ages") },
    { step: 2, label: strings.questionThreeTitle, value: getQuestionSummary("groupSize") },
    { step: 3, label: strings.questionFourTitle, value: getQuestionSummary("style") }
  ].filter((answer) => answer.value);

  status.textContent = answers.length ? answers.length + " / 4" : strings.profileEmpty;
  profile.hidden = answers.length === 0;
  list.replaceChildren();
  answers.forEach((answer) => {
    const chip = document.createElement("button");
    chip.type = "button";
    chip.className = "quest-profile__chip";
    chip.dataset.answerChip = "true";
    chip.dataset.editStep = String(answer.step);
    chip.textContent = answer.value;
    chip.setAttribute("aria-label", answer.label + ": " + answer.value + ". " + strings.profileChange);
    list.appendChild(chip);
  });
}

function renderFinderState() {
  const root = getDialogRoot();
  if (!root) return;
  const strings = copy[currentLanguage];
  const activeStep = finderState.activeStep;
  const isIntro = finderState.dialogMode === "intro";
  const isComplete = finderState.dialogMode === "complete" && isFinderComplete();
  const isQuestion = finderState.dialogMode === "question" && !isComplete;
  const intro = root.querySelector("[data-dialog-intro]");
  const questionShell = root.querySelector("[data-dialog-question-shell]");
  const completion = root.querySelector("[data-dialog-completion]");
  const navigation = root.querySelector("[data-dialog-actions]");

  if (intro) intro.hidden = !isIntro;
  if (questionShell) questionShell.hidden = !isQuestion;
  if (completion) completion.hidden = !isComplete;
  if (navigation) navigation.hidden = !isQuestion;

  root.querySelectorAll("[data-finder-step]").forEach((question) => {
    const step = Number(question.dataset.finderStep);
    const isActive = isQuestion && step === activeStep;
    question.hidden = !isActive;
    question.classList.toggle("is-active", isActive);
    question.setAttribute("aria-current", isActive ? "step" : "false");
  });

  root.querySelectorAll("[data-finder-answer]").forEach((option) => {
    const group = option.dataset.finderGroup;
    const value = option.dataset.finderValue;
    const selected = group === "ages" ? finderState.ages.includes(value) : finderState[group] === value;
    option.classList.toggle("is-selected", selected);
    option.setAttribute("aria-pressed", String(selected));
  });

  const backButton = root.querySelector('[data-action="finder-back"]');
  const continueButton = root.querySelector('[data-action="finder-continue"]');
  if (backButton) backButton.disabled = activeStep === 0 || !isQuestion;
  if (continueButton) {
    continueButton.textContent = strings.dialogContinue;
    continueButton.disabled = !isQuestion || !isStepAnswered(activeStep);
  }

  const dialogStatus = root.querySelector("[data-dialog-status]");
  if (dialogStatus && isStepAnswered(activeStep)) dialogStatus.textContent = "";

  renderQuestProfile();
}

function getQuestgiverMessageKey() {
  if (finderState.dialogMode === "complete" && isFinderComplete()) return "questgiverCompletionReaction";
  if (finderState.dialogMode === "intro") return "questgiverGreeting";
  return ["questgiverAskAudience", "questgiverAskAges", "questgiverAskGroup", "questgiverAskStyle"][finderState.activeStep];
}

function renderQuestgiverMessage() {
  const message = document.querySelector("[data-questgiver-message]");
  if (!message) return;
  const key = getQuestgiverMessageKey();
  message.textContent = copy[currentLanguage][key] || copy[currentLanguage].questgiverGreeting;
  message.classList.remove("is-refreshing");
  requestAnimationFrame(() => message.classList.add("is-refreshing"));
}

function resetFrom(step) {
  if (step <= 1) {
    finderState.ages = [];
    finderState.agesConfirmed = false;
  }
  if (step <= 2) finderState.groupSize = null;
  if (step <= 3) finderState.style = null;
}

function focusDialogStep(shouldScroll) {
  const root = getDialogRoot();
  const question = root && root.querySelector('[data-finder-step="' + finderState.activeStep + '"]');
  const firstAnswer = question && question.querySelector("[data-finder-answer]");
  if (shouldScroll && question) question.scrollIntoView({ behavior: "smooth", block: "center" });
  if (firstAnswer) requestAnimationFrame(() => firstAnswer.focus());
}

function selectFinderOption(button) {
  if (!button || !button.closest("[data-finder-dialog]")) return;
  if (finderState.dialogMode !== "question") return;
  const group = button.dataset.finderGroup;
  const value = button.dataset.finderValue;
  const step = Number(button.closest("[data-finder-step]").dataset.finderStep);

  if (group === "ages") {
    const wasConfirmed = finderState.agesConfirmed;
    if (finderState.ages.includes(value)) finderState.ages = finderState.ages.filter((age) => age !== value);
    else finderState.ages = [...finderState.ages, value].sort((a, b) => ageOrder.indexOf(a) - ageOrder.indexOf(b));
    finderState.agesConfirmed = false;
    if (wasConfirmed) resetFrom(2);
  } else {
    const changed = finderState[group] !== value;
    finderState[group] = value;
    if (changed) resetFrom(step + 1);
  }

  finderState.activeStep = step;
  finderState.dialogMode = "question";
  finderState.recommendationVisible = false;
  finderState.recommendationApplied = false;
  finderState.openPlayType = null;
  renderFinderState();
  renderPlayTypes();
}

function startFinder() {
  finderState.dialogMode = "question";
  finderState.activeStep = 0;
  finderState.recommendationVisible = false;
  finderState.recommendationApplied = false;
  finderState.openPlayType = null;
  renderFinderState();
  renderQuestgiverMessage();
  focusDialogStep(true);
}

function continueFinder() {
  if (finderState.dialogMode !== "question") return;
  const step = finderState.activeStep;
  const strings = copy[currentLanguage];

  if (!isStepAnswered(step)) {
    const status = document.querySelector("[data-dialog-status]");
    if (status) status.textContent = step === 1 ? strings.ageValidation : "";
    return;
  }

  if (step === 1) finderState.agesConfirmed = true;

  if (step < 3) {
    finderState.activeStep = step + 1;
    renderFinderState();
    renderQuestgiverMessage();
    focusDialogStep(true);
    return;
  }

  finderState.recommendationVisible = true;
  finderState.dialogMode = "complete";
  finderState.recommendationApplied = false;
  finderState.openPlayType = null;
  renderFinderState();
  renderQuestgiverMessage();
  renderPlayTypes();
  requestAnimationFrame(() => {
    const completionButton = document.querySelector('[data-action="show-quests"]');
    if (completionButton) completionButton.focus({ preventScroll: true });
  });
}

function goBackFinder() {
  if (finderState.dialogMode !== "question" || finderState.activeStep === 0) return;
  finderState.activeStep -= 1;
  finderState.recommendationVisible = false;
  finderState.recommendationApplied = false;
  finderState.openPlayType = null;
  renderFinderState();
  renderQuestgiverMessage();
  renderPlayTypes();
  focusDialogStep(true);
}

function editFinderStep(button) {
  const step = Number(button && (button.dataset.editStep || button.dataset.finderStep));
  if (!Number.isInteger(step)) return;
  finderState.activeStep = Math.max(0, Math.min(step, 3));
  finderState.dialogMode = "question";
  finderState.recommendationVisible = false;
  finderState.recommendationApplied = false;
  finderState.openPlayType = null;
  renderFinderState();
  renderQuestgiverMessage();
  renderPlayTypes();
  focusDialogStep(true);
}

function getRecommendationCandidates() {
  if (!isFinderComplete()) return [];
  const youngest = getYoungestAge();
  const source = recommendationMatrix[youngest]?.[finderState.groupSize]?.[finderState.style] || [];
  const candidates = [];
  const addCandidate = (candidate) => {
    if (candidate && !candidates.includes(candidate) && copy[currentLanguage].playCatalog[candidate]) candidates.push(candidate);
  };

  source.forEach(addCandidate);
  (fallbackCandidates[source[0]] || []).forEach(addCandidate);
  ["forestMonster", "waterBattle", "captureFlag", "vipEscort", "kingHill", "attackDefend", "treasureHunt", "storyAdventure", "lostPlace", "deduction", "operationAlbtal"].forEach(addCandidate);
  return candidates.slice(0, 3);
}

function getRecommendedPlayTypes() {
  const families = [];
  const familyOrder = ["monsterHunt", "teamBattles", "adventure", "deduction", "missions"];
  const addFamily = (candidate) => {
    const family = candidateFamilies[candidate] || candidate;
    if (familyOrder.includes(family) && !families.includes(family)) families.push(family);
  };

  getRecommendationCandidates().forEach(addFamily);
  familyOrder.forEach(addFamily);
  return families.slice(0, 3);
}

