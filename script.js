let currentLanguage = "de";
const compassState = { audience: null, style: null };

function getValue(source, path) {
  return path.split(".").reduce((value, key) => value && value[key], source);
}

function applyLanguage(language) {
  currentLanguage = language;
  const strings = copy[language];
  document.documentElement.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(strings, element.dataset.i18n);
    if (typeof value === "string") element.textContent = value;
  });

  document.querySelectorAll("[data-aria-i18n]").forEach((element) => {
    const value = getValue(strings, element.dataset.ariaI18n);
    if (typeof value === "string") element.setAttribute("aria-label", value);
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  const menuToggle = document.querySelector(".menu-toggle");
  if (menuToggle) {
    const menuIsOpen = menuToggle.getAttribute("aria-expanded") === "true";
    menuToggle.setAttribute("aria-label", menuIsOpen ? strings.menuClose : strings.menuOpen);
    const hiddenText = menuToggle.querySelector("[data-i18n]");
    if (hiddenText) hiddenText.textContent = menuIsOpen ? strings.menuClose : strings.menuOpen;
  }

  renderRecommendation();
}

function audienceLabel(value) {
  const key = { school: "audienceSchool", event: "audienceEvent", private: "audiencePrivate" }[value];
  return key ? getValue(copy[currentLanguage], key) : "";
}

function renderRecommendation() {
  const emptyState = document.querySelector("[data-recommendation-empty]");
  const resultState = document.querySelector("[data-recommendation-result]");
  const style = compassState.style;

  if (!style) {
    emptyState.hidden = false;
    resultState.hidden = true;
    return;
  }

  const recommendation = copy[currentLanguage].recommendation[style];
  const resultKicker = resultState.querySelector('[data-result="kicker"]');
  const resultTitle = resultState.querySelector('[data-result="title"]');
  const resultDescription = resultState.querySelector('[data-result="description"]');
  const resultMeta = resultState.querySelector('[data-result="meta"]');
  const resultAudience = resultState.querySelector('[data-result="audience"]');

  resultKicker.textContent = recommendation.kicker;
  resultTitle.textContent = recommendation.title;
  resultDescription.textContent = recommendation.description;
  resultMeta.textContent = recommendation.meta;
  resultAudience.textContent = compassState.audience ? `${copy[currentLanguage].recommendationAudiencePrefix}: ${audienceLabel(compassState.audience)}` : "";
  emptyState.hidden = true;
  resultState.hidden = false;

  document.querySelectorAll("[data-mission-card]").forEach((card) => {
    card.classList.toggle("is-focused", card.dataset.missionCard === recommendation.mission);
  });
}

function selectCompassOption(button) {
  const group = button.dataset.compassGroup;
  const value = button.dataset.value;
  compassState[group] = value;

  document.querySelectorAll(`[data-compass-group="${group}"]`).forEach((option) => {
    option.classList.toggle("is-selected", option === button);
    option.setAttribute("aria-pressed", String(option === button));
  });

  const question = button.closest(".compass-question");
  question.classList.add("is-complete");
  const nextQuestion = question.nextElementSibling;
  if (nextQuestion && nextQuestion.classList.contains("compass-question")) nextQuestion.classList.add("is-active");
  renderRecommendation();
}

function closeMobileMenu() {
  const nav = document.querySelector(".primary-nav");
  const menuToggle = document.querySelector(".menu-toggle");
  nav.classList.remove("is-open");
  menuToggle.setAttribute("aria-expanded", "false");
  applyLanguage(currentLanguage);
}

function simulateContact() {
  const status = document.querySelector("#contact-status");
  status.textContent = copy[currentLanguage].contactSimulation;
  status.scrollIntoView({ behavior: "smooth", block: "nearest" });
}

document.addEventListener("DOMContentLoaded", () => {
  applyLanguage(currentLanguage);

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });

  document.querySelectorAll("[data-compass-group]").forEach((button) => {
    button.setAttribute("aria-pressed", "false");
    button.addEventListener("click", () => selectCompassOption(button));
  });

  document.querySelectorAll('[data-action="select-mission"]').forEach((button) => {
    button.addEventListener("click", () => {
      const style = button.dataset.mission;
      compassState.style = style;
      renderRecommendation();
      document.querySelector("#recommendation").scrollIntoView({ behavior: "smooth", block: "center" });
    });
  });

  document.querySelectorAll('[data-action="simulate-contact"]').forEach((button) => button.addEventListener("click", simulateContact));

  const menuToggle = document.querySelector(".menu-toggle");
  menuToggle.addEventListener("click", () => {
    const isOpen = menuToggle.getAttribute("aria-expanded") === "true";
    menuToggle.setAttribute("aria-expanded", String(!isOpen));
    document.querySelector(".primary-nav").classList.toggle("is-open", !isOpen);
    applyLanguage(currentLanguage);
  });

  document.querySelectorAll(".primary-nav a").forEach((link) => link.addEventListener("click", closeMobileMenu));

  const header = document.querySelector("[data-header]");
  const updateHeader = () => header.classList.toggle("is-scrolled", window.scrollY > 22);
  updateHeader();
  window.addEventListener("scroll", updateHeader, { passive: true });
});
